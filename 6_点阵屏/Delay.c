#include <REGX52.H>

/****************************************************************************************************
功能：循环以达到延时效果
参数：mill 要延时的毫秒
返回值：无
****************************************************************************************************/
void Delay(unsigned int mill)		//@11.952MHz
{
	while(mill--){
		unsigned char i, j;

		i = 2;
		j = 237;
		do
		{
			while (--j);
		} while (--i);
	}
}
